// __dirname

console.log(__dirname); // It will print the current directory path

// module
console.log(module); // It will print the full path of the current module file 

console.log(module.id); // It will print the module id, usually '.' 

console.log(process); // It will print the process object which contains information about the current Node.js process

console.log(required); // It will print the function used to import modules in Node.js

console.log(global); // It will print the global object in Node.js which contains all global variables and functions

console.log(Buffer); // It will print the Buffer class used to handle binary data in Node.js

